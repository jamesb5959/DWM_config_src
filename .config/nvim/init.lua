vim.api.nvim_create_autocmd("BufEnter", {
	callback = function()
		local bufnr = vim.api.nvim_get_current_buf()
		local bufname = vim.api.nvim_buf_get_name(bufnr)
		if bufname:match("NvimTree_") then
			if #vim.api.nvim_list_wins() == 1 then
				vim.cmd("quit")
			end
		end
	end,
})
